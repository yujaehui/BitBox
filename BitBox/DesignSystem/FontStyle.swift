import UIKit

enum FontStyle {
    static let largeTitle: UIFont = .boldSystemFont(ofSize: 34)
    static let semiLargeTitle: UIFont = .boldSystemFont(ofSize: 25)
    static let title: UIFont = .systemFont(ofSize: 20, weight: .heavy)
    static let semiTitle: UIFont = .systemFont(ofSize: 20, weight: .regular)
    
    static let primaryBold: UIFont = .boldSystemFont(ofSize: 17)
    static let primary: UIFont = .systemFont(ofSize: 17)
    static let secondaryBold: UIFont = .boldSystemFont(ofSize: 14)
    static let secondary: UIFont = .systemFont(ofSize: 14)
}
