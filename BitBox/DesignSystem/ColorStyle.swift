import UIKit

enum ColorStyle {
    static let black = UIColor(hexCode: "#000000")
    static let white = UIColor(hexCode: "#FFFFFF")
    static let red = UIColor(hexCode: "#F04452")
    static let blue = UIColor(hexCode: "#3282F8")
    static let purple = UIColor(hexCode: "#914CF5")
    static let gray = UIColor(hexCode: "#828282")
    static let darkGray = UIColor(hexCode: "#343D4C")
    static let lightGray = UIColor(hexCode: "#F3F4F6")
    static let babyPink = UIColor(hexCode: "#FFEAED")
    static let skyBlue = UIColor(hexCode: "#E5F0FF")
}
